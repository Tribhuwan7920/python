# arthmatical operator
print(5+3)
print(5-3)
print(5*3)
print(5/3)
print(5**3)
print(5//3)
print(5%3)

# assignment operator
age = 1
print(age)
age += 1
print(age)
age *= 1
print(age)
age -= 1
print(age)
age **= 2
print(age)
age /= 2
print(age)

# comparison operator  
print(5>4)
print(5<4)
print(5==4)
print(5 is 4)
print(5 != 4)
print(5 >= 4)
print(5 <= 4)

# logical opperator 

# logical and operator
print(True and  True )
print(True and  False )
print(False and  True )
